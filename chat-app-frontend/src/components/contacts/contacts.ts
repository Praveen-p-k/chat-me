export const getMedianFromTime = (timeString: string) => {
  // Extract the hour from the time string
  const time = timeString.split(" ")[1];
  const hour = parseInt(time.split(":")[0], 10);

  // Determine AM or PM
  let period;
  if (hour === 12) {
    period = "PM"; // Assuming 12:00 PM is noon
  } else if (hour < 12) {
    period = "AM";
  } else {
    period = "PM"; // All hours greater than 12 are PM
  }

  return { time, period }; // Output: "PM"
};
